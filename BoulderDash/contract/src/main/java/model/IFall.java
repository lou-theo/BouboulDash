public interface IFall {

	/**
	 * Give the coordinate x of the IFall element
	 * @return int x
	 */
	int getX();

	/**
	 * Give the coordinate y of the IFall element
	 * @return int y
	 */
	int getY();

	/**
	 * Set the coordinate x of the IFall element
	 * @param i
	 */
	void setX(int i);

	/**
	 * Set the coordinate y of the IFall element
	 * @param i
	 */
	void setY(int i);

}